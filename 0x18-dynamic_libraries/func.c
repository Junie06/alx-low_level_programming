#include "main.h"

/**
 * add - function that adds two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function that give the difference of two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: the result
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - function that multiplies two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: the result
 */
int mul(int a, int b)
{
	int result =  a * b;

	return (result);
}

/**
 * div - function that divides two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: the result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mol - function that gived the remainder from the division two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: the result
 */
int mol(int a, int b)
{
	return (a % b);
}
